// Helper functions for filtering and searching countries
export const getCountriesByRegion = (region) => {
  return countries.filter(country => country.region.toLowerCase() === region.toLowerCase());
};

export const getCountriesByContinent = (continent) => {
  return countries.filter(country => country.continent.toLowerCase() === continent.toLowerCase());
};

export const searchCountries = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return countries.filter(country => 
    country.name.toLowerCase().includes(term) ||
    country.capital.toLowerCase().includes(term) ||
    country.languages.some(lang => lang.toLowerCase().includes(term))
  );
};

export const getCountryById = (id) => {
  return countries.find(country => country.id === id);
};

export const sortCountriesByPopulation = (ascending = false) => {
  return [...countries].sort((a, b) => 
    ascending ? a.population - b.population : b.population - a.population
  );
};

export const sortCountriesByArea = (ascending = false) => {
  return [...countries].sort((a, b) => 
    ascending ? a.area - b.area : b.area - a.area
  );
};

export const sortCountriesByGDP = (ascending = false) => {
  return [...countries].sort((a, b) => 
    ascending ? a.gdp - b.gdp : b.gdp - a.gdp
  );
};

// Format numbers for display
export const formatNumber = (num) => {
  return new Intl.NumberFormat().format(num);
};

export const formatPopulation = (population) => {
  if (population >= 1000000000) {
    return `${(population / 1000000000).toFixed(1)}B`;
  } else if (population >= 1000000) {
    return `${(population / 1000000).toFixed(1)}M`;
  } else if (population >= 1000) {
    return `${(population / 1000).toFixed(1)}K`;
  }
  return population.toString();
};

export const formatArea = (area) => {
  return `${formatNumber(area)} km`;
};

export const formatGDP = (gdp) => {
  if (gdp >= 1000000) {
    return `$${(gdp / 1000000).toFixed(1)}T`;
  } else if (gdp >= 1000) {
    return `$${(gdp / 1000).toFixed(1)}B`;
  }
  return `$${gdp}M`;
};
